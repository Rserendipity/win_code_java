public class Message {
    private String who;
    private String toWho;
    private int time;
    private String msg;
    private String who_pic;
    private String toWho_pic;

    public String getWho() {
        return who;
    }

    public void setWho(String who) {
        this.who = who;
    }

    public String getToWho() {
        return toWho;
    }

    public void setToWho(String toWho) {
        this.toWho = toWho;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getWho_pic() {
        return who_pic;
    }

    public void setWho_pic(String who_pic) {
        this.who_pic = who_pic;
    }

    public String getToWho_pic() {
        return toWho_pic;
    }

    public void setToWho_pic(String toWho_pic) {
        this.toWho_pic = toWho_pic;
    }
}